
class MultiDimentional
{
    public static void main(String A[])
    {
        int Arr[] = new int[4];
        int Brr[][] = new int[3][4];

        System.out.println(Arr.length);     // 4
        System.out.println(Brr.length);     // 3
        System.out.println(Brr[0].length);  // 4
    
    }
}